# Arithmatic operators in Python

x = 5
y = 2

Sum = x + y    # Addition
print("Sum: ", Sum)

sub = x - y    # Subtraction
print("Subtract: ", sub)

mul = x * y    # Multiply
print("Multiply: ", mul)

div = x / y    # Division
print("Division: ", div)

exp = x ** y    # Exponential
print("Exponential: ", exp)

# Floor division: returns integer part of division
fdiv = x // y
print("Floor division: ", fdiv)

# Modulo: returns remainder of division
mod = x % y
print("Remainder: ", mod)
