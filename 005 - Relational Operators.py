# Comparison or Relational Operators
# These return True or False

a = 5
b = 5
c = 2

# Equal to    '=='
print("a == b: ", a == b)
print("b == c: ", b == c)

# Not equal to    '!='
print("a != b: ", a != b)
print("b != c: ", b != c)

# Greater than    '>'
print("a > b: ", a > b)
print("b > c: ", b > c)

# Greater than or equal to    '>='
print("a >= b: ", a >= b)
print("b >= c: ", b >= c)

# Less than    '<'
print("a < b: ", a < b)
print("b < c: ", b < c)

# Less than or equal to    '<='
print("a <= b: ", a <= b)
print("b <= c: ", b <= c)
