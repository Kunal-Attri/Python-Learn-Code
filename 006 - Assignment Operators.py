# Assignment operators

a = 5
b = 2

# '=' operator assigns the value on the right to the
# variable on the left

c = a + b
print(c)

# '+=' operator assigns the value of operand on left +
# operand on right to the operand(variable) on left

# eg. a += b is same as a = a + b

print(a)
a += b
print(a)

a = 5
b = 2
print(a)
a = a + b
print(a)

# Similarly, it can be used with -, *, /, //, %, **(exp)
# Like, a -= b, a *= b, a /= b, a //= b, a %= b, a **= b

