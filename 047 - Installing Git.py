# Installing Git on Windows

# Step 1 - Open Browser, go to https://www.git-scm.com/downloads/
# Step 2 - Click on your OS type (Windows in our case)
# Step 3 - Let the file download, and then open it
# Step 4 - Click Next, Next, Next... Install (No need to change anything)
# Step 5 - You are done and ready to use Git!

